  certification along with their global acceptability. The basic principle of the system is that it is + scientific, objective and
  transparent with least possibility of subjectivity caused due to the individual evaluator, particular subject or a particular
  batch of students.</p>
  <br />
  <p>The examination system is divided as follows:</p>
  <br />
  <h2>Theory Examination</h2>
  <p>1. Internal Assessment: It includes 1st hourly test, Mid-term test, 2nd hourly test, surprise test, quiz, tutorials and
  assignments.</p>
  <br />
  <p>2. External Assessment: At the end of semester, final examination will be conducted to evaluate students performance.</p>
