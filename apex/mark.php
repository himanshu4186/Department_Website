    conduct of experiments during the practical class.</li>
    <li>A student absent during the conduct of experiment will not be given any credit for the practical and shall get zero
    marks.</li>
    <li>The marks in all the experiments conducted during semester will be added.</li>
    <li>The total marks obtained by the student shall be reduced out of 45marks.</li>
    <li>15 marks shall be allotted to internal practical tests to be held twice during the semester.</li>
    <li>First internal practical test will have 5 marks and shall be conducted after completion of 1/3rd of the practicals. The
