  <h2>Practical Papers</h2>
  <h3>A. Internal Assessment/Evaluation</h3>
  <ul class="list-style-romon">
    <li>Each lab has 10 to 12 experiments during a semester in every practical subject and students are required to conduct all the
    experiments.</li>
    <li>Each experiment is to be performed by the student himself in practical class.</li>
    <li>Each semester is divided into 3 segments as Unit -1, Unit-2 and Unit-3 for the purpose of evaluation.</li>
    <li>Each experiment is evaluated in following three components:
    <br />
