  <h3>Cumulative Grade Point Average</h3>
  <p>CGPA is the weighted average of all the grades awarded to the student since his/her entry into the university up to and
  including latest semester. CGPA =; Ci = Credits for ith paper Gi = Grade points of the grade earned in ith paper</p>
  <br />
  <hr />
  <h2>Promotion for Next Session and Condition for Year Back</h2>
  <h3>For Undergraduate Courses</h3>
  <p>A student will be allowed to continue in the program only if, at the end of first year he/she secures CGPA of 4.00 or more,
  similarly at the end of 2nd year onwards. He/she secures CGPA of greater than or equal to 4.50. Lateral Entry students are
  treated as second year students. Students who are unable to fulfill the minimum requirement of CGPA shall lose one year.</p>
  <br />
  <h3>For Post-graduate Courses</h3>In case of Post Graduation, minimum required CGPA at the end of first year is 4.50. Students
  who are unable to fulfill this requirement shall lose one year.
  <br />
  <hr />
  <h2>Requirement for award of degree</h2>
  <h3>For Undergraduate Courses</h3>
  <p>A student is deemed to have completed the requirements for a program and is eligible for the award of degree if, she/he has
  earned a CGPA of greater than or equal to 4.50 for UG programs.</p>
  <br />
  <h3>For Post-graduate Courses</h3>
  <p>The minimum CGPA required for the award of all Post-graduate degrees is 5.0.</p>
