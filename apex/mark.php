  <h2>Salient Features</h2>
  <p>The salient features of the examination system are listed below:</p>
  <br>
  <ul class="listt-style">
    <li>The papers for the final exams would be set by the external examiners, who would not be a part of Chandigarh University.
    The panel of paper setters would be appointed by the University authorities.</li>
    <li>The evaluation of the final semester examination would be done through Table Marking at the University. Question wise
    evaluation will be done to maintain uniformity.</li>
    <li>10% of the answer books shall be randomly evaluated by outside experts to ensure fairness and equity.</li>
    <li>The evaluated answer books shall be shown to the students. The student will see &amp; sign the Answer books for having
    agreed to the evaluation.</li>
    <li>In case a student disagrees with the evaluation, he/she can get the answer sheet reviewed by the 
    <strong>Evaluation Review Board</strong> constituted for the same purpose, on payment of prescribed fee. No change in marks
    shall be done by any individual teacher.</li>
    <li>The decision of Evaluation Review Board shall be final and binding.</li>
    <li>If the increase in marks is recommended by the 
    <strong>Evaluation Review Board</strong> is more than 10% of the maximum marks for the subject, the fee submitted for the
    review shall be refunded to the students.</li>
    <li>Final Internal Evaluation shall be displayed by the departments/Institutes, before submission to the University.</li>
    <li>The total marks obtained in Internal and External Examination shall be submitted to the Registrar Office for conversion
    into grades based on relative grading system.</li>
    <li>Grade shall be awarded to the obtained marks in each paper using Normal Distribution, without any subjectivity.</li>
    <li>A student getting less than 35% of the highest marks obtained in the class shall be awarded F grade.</li>
    <li>A student getting more than 50% of the highest marks scored in the class shall not get F Grade.</li>
    <li>The final grades of the semester will be displayed along with SGPA (Semester Grade Point Average) at the time of
    declaration of results.</li>
  </ul>
  <br />
